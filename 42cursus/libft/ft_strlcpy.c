#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	return_length;

	return_length = 0;
	while (src[return_length] != 0)
		return_length++;
	i = 0;
	if (dstsize > 0)
	{
		while (src[i] != 0 && i < dstsize - 1)
		{
			dst[i] = src[i];
			i++;
		}
		dst[i] = 0;
	}
	return (return_length);
}
